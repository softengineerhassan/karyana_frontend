// Process each file
function processFile(filePath) {
  let content = fs.readFileSync(filePath, 'utf8');
  const lines = content.split('\n');
  let changes = 0;
  
  for (let i = 0; i < lines.length; i++) {
    // Match Fr field with single-quoted string value
    const frMatch = lines[i].match(/^(\s*\w+Fr:\s*)'((?:[^'\\]|\\.)*?)('.*)/);
    if (!frMatch) continue;
    
    const prefix = frMatch[1];
    const currentValue = frMatch[2];
    const suffix = frMatch[3];
    
    // Look up translation
    if (translations[currentValue] && translations[currentValue] !== currentValue) {
      const newValue = translations[currentValue];
      // Escape single quotes in the translation
      const escaped = newValue.replace(/'/g, "\\'");
      lines[i] = `${prefix}'${escaped}${suffix}`;
      changes++;
    }
  }
  
  if (changes > 0) {
    fs.writeFileSync(filePath, lines.join('\n'));
    console.log(`${filePath}: ${changes} fields translated to French`);
  } else {
    console.log(`${filePath}: no changes needed`);
  }
  
  return changes;
}

