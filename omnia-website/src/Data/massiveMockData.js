const generateName = (index) => {
  const firstIdx = index % firstNames.length;
  const lastIdx = Math.floor(index / firstNames.length) % lastNames.length;
  return `${firstNames[firstIdx]} ${lastNames[lastIdx]}`;
};

const generateEmail = (name, index) => {
  return `${name.toLowerCase().replace(/\s+/g, '.')}${index > 50 ? index : ''}@email.com`;
};

