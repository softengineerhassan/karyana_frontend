// Helper to generate random booking
const generateBooking = (index, venueId, userId) => {
  const statuses = ['pending', 'confirmed', 'confirmed', 'completed', 'completed', 'completed', 'cancelled'];
  const randomStatus = statuses[Math.floor(Math.random() * statuses.length)];
  
  const baseDate = new Date();
  baseDate.setDate(baseDate.getDate() + Math.floor(Math.random() * 60) - 30); // +/- 30 days
  
  return {
    id: `booking-massive-${index}`,
    venueId,
    userId,
    date: baseDate.toISOString().split('T')[0],
    time: `${12 + Math.floor(Math.random() * 8)}:${['00', '30'][Math.floor(Math.random() * 2)]}`,
    partySize: 2 + Math.floor(Math.random() * 6),
    status: randomStatus,
    specialRequests: Math.random() > 0.7 ? 'Window table preferred' : '',
    createdAt: new Date(baseDate.getTime() - 7 * 24 * 60 * 60 * 1000).toISOString(),
    totalAmount: 50 + Math.floor(Math.random() * 300),
    statusHistory: [
      {
        status: 'pending',
        timestamp: new Date(baseDate.getTime() - 7 * 24 * 60 * 60 * 1000).toISOString(),
        note: 'Booking created'
      }
    ]
  };
};

